void main() {
  
  // Menyimpan panjang dan lebar
  double panjang = 10;
  double lebar = 5;

  // Rumus menghitung luas dan keliling
  double luas = panjang * lebar;
  double keliling = 2 * (panjang + lebar);

  // Menampilkan hasil
  print("Panjang = $panjang");
  print("Lebar = $lebar");
  print("Luas = $luas");
  print("Keliling = $keliling");

}